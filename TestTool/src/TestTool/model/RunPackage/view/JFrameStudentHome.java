    public JFrameStudentHome(StudentRun studentRun) 
    {   
        this.studentRun = studentRun; 
        //now got the student. Can work on student
        student = studentRun.getStudent();
        
        this.takenTests = studentRun.getCompleteTest();
        this.availableTests = studentRun.getAvailableTest();
        this.testIndex = 0;
        model = new DefaultTableModel();
        
        model.addColumn("Test");
        model.addColumn("Status");
        model.addColumn("Grade");
        
        for (int i = 0; i < takenTests.size(); i++) {
            model.addRow(new Object[] {takenTests.get(i).getTestName(), 
                                        "Complete",takenTests.get(i).getPoint()});
        }
        

        initComponents();
        jLabelStudentHome.setText(student.getName());
        
        //Set the model for the GUI table
    }
    
