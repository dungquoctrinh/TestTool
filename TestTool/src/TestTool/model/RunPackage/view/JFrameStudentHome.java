        testInfo = new DefaultTableModel();
        testInfo.addColumn("Instructor");
        testInfo.addColumn("Type");
        testInfo.addColumn("Time");
        testInfo.addColumn("Difficulty");
        testInfo.addColumn("Due Date");
        
